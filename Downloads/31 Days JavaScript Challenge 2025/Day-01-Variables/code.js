// Day 1: JavaScript Basics - Variables

// var (old style), let (modern), const (constant)
var name = "Shobhit";     // var => purana style, avoid karo
let age = 22;             // let => flexible value (can change)
const country = "India";  // const => fixed value (cannot change)

// Printing values
console.log("Name:", name);
console.log("Age:", age);
console.log("Country:", country);

// Updating let
age = 23;
console.log("Updated Age:", age);

